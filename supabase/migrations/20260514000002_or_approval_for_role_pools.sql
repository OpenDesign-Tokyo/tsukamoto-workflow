-- ─────────────────────────────────────────────────────────────────────────────
-- 複数候補者プールから1人で済むステップを OR承認 (approval_type='any') に変更
-- ─────────────────────────────────────────────────────────────────────────────
-- 「生産担当」「担当パタンナー」は組織内に複数名いる役職で、いずれか1名の承認
-- で先に進めるのが実運用に近い。これまで approval_type='single' のため最初の
-- 候補者だけに通知が飛んでいたが、'any' にすると候補者全員に通知され、最初に
-- 承認した1名で次へ進む（残りは自動スキップ）。
--
-- 対象は既存ルートに限定。今後追加するルートは admin/routes 編集画面で個別
-- に設定する。
-- ─────────────────────────────────────────────────────────────────────────────

BEGIN;

UPDATE approval_route_steps
SET approval_type = 'any'
WHERE approval_type IS DISTINCT FROM 'any'
  AND (
    name LIKE '%生産担当%'
    OR name LIKE '%担当パタンナー%'
  );

-- 既存の進行中申請には影響しない (approval_records は既に作成済みのため)。
-- このマイグレーション後に新規提出される申請から OR承認が適用される。

COMMIT;
